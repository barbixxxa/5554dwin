function media(a, b) {
    return (a + b) / 2
}

function mediaComPorcentagem(porcentagemPrimeiraNota, primeiraNota, porcentagemSegundaNota, segundaNota) {
    return (porcentagemPrimeiraNota * primeiraNota) + (porcentagemSegundaNota * segundaNota)
}

